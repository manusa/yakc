package com.marcnuri.yakc.model.io.k8s.metrics.pkg.apis.metrics.v1beta1;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.marcnuri.yakc.model.Model;
import java.util.Map;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;
import lombok.Singular;
import lombok.ToString;

/**
 * ContainerMetrics sets resource usage metrics of a container.
 */
@SuppressWarnings({"squid:S1192", "WeakerAccess", "unused"})
@Builder(toBuilder = true)
@AllArgsConstructor
@NoArgsConstructor
@Data
@ToString
public class ContainerMetrics implements Model {


  /**
   * Container name corresponding to the one from pod.spec.containers.
   */
  @NonNull
  @JsonProperty("name")
  private String name;

  /**
   * The memory usage is the memory working set.
   */
  @NonNull
  @JsonProperty("usage")
  @Singular(value = "putInUsage", ignoreNullCollections = true)
  private Map<String, String> usage;

}

