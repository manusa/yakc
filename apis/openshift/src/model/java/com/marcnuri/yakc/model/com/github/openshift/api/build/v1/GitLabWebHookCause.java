package com.marcnuri.yakc.model.com.github.openshift.api.build.v1;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.marcnuri.yakc.model.Model;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.ToString;

/**
 * GitLabWebHookCause has information about a GitLab webhook that triggered a build.
 */
@SuppressWarnings({"squid:S1192", "WeakerAccess", "unused"})
@Builder(toBuilder = true)
@AllArgsConstructor
@NoArgsConstructor
@Data
@ToString
public class GitLabWebHookCause implements Model {


  @JsonProperty("revision")
  private SourceRevision revision;

  /**
   * Secret is the obfuscated webhook secret that triggered a build.
   */
  @JsonProperty("secret")
  private String secret;

}

