package com.marcnuri.yakc.model.com.github.operatorframework.operatorlifecyclemanager.pkg.api.apis.operators.v1alpha1;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.marcnuri.yakc.model.Model;
import java.util.List;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.Singular;
import lombok.ToString;

/**
 * APIServiceDefinitions declares all of the extension apis managed or required by an operator being ran by ClusterServiceVersion.
 */
@SuppressWarnings({"squid:S1192", "WeakerAccess", "unused"})
@Builder(toBuilder = true)
@AllArgsConstructor
@NoArgsConstructor
@Data
@ToString
public class APIServiceDefinitions implements Model {


  @JsonProperty("owned")
  @Singular(value = "addToOwned", ignoreNullCollections = true)
  private List<APIServiceDescription> owned;

  @JsonProperty("required")
  @Singular(value = "addToRequired", ignoreNullCollections = true)
  private List<APIServiceDescription> required;

}

