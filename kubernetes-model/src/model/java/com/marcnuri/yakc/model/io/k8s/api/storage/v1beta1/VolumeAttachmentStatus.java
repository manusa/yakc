package com.marcnuri.yakc.model.io.k8s.api.storage.v1beta1;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.marcnuri.yakc.model.Model;
import java.util.Map;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;
import lombok.Singular;
import lombok.ToString;

/**
 * VolumeAttachmentStatus is the status of a VolumeAttachment request.
 */
@SuppressWarnings({"squid:S1192", "WeakerAccess", "unused"})
@Builder(toBuilder = true)
@AllArgsConstructor
@NoArgsConstructor
@Data
@ToString
public class VolumeAttachmentStatus implements Model {


  @JsonProperty("attachError")
  private VolumeError attachError;

  /**
   * Indicates the volume is successfully attached. This field must only be set by the entity completing the attach operation, i.e. the external-attacher.
   */
  @NonNull
  @JsonProperty("attached")
  private Boolean attached;

  /**
   * Upon successful attach, this field is populated with any information returned by the attach operation that must be passed into subsequent WaitForAttach or Mount calls. This field must only be set by the entity completing the attach operation, i.e. the external-attacher.
   */
  @JsonProperty("attachmentMetadata")
  @Singular("putInAttachmentMetadata")
  private Map<String, String> attachmentMetadata;

  @JsonProperty("detachError")
  private VolumeError detachError;

}

