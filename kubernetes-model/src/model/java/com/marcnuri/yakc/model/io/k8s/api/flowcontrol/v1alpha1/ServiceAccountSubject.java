package com.marcnuri.yakc.model.io.k8s.api.flowcontrol.v1alpha1;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.marcnuri.yakc.model.Model;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.NonNull;
import lombok.ToString;

/**
 * ServiceAccountSubject holds detailed information for service-account-kind subject.
 */
@SuppressWarnings({"squid:S1192", "WeakerAccess", "unused"})
@Builder(toBuilder = true)
@AllArgsConstructor
@NoArgsConstructor
@Data
@ToString
public class ServiceAccountSubject implements Model {


  /**
   * `name` is the name of matching ServiceAccount objects, or "&#42;" to match regardless of name. Required.
   */
  @NonNull
  @JsonProperty("name")
  private String name;

  /**
   * `namespace` is the namespace of matching ServiceAccount objects. Required.
   */
  @NonNull
  @JsonProperty("namespace")
  private String namespace;

}

